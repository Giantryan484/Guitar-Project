# Initialize TABs
TABs = {
    "E": [],
    "A": [],
    "D": [],
    "G": [],
    "B": [],
    "e": []
}

# Plot notes on the TABs
for j in range(condensed_data.shape[1]): # for every time entry
    note_started = False
    for i in range(len(condensed_data)): # for every note 
        if condensed_data[i, j] == 1 and not note_started: # if new and present
            string_fret = note_to_string_fret[j].split("|")
            string = string_fret[0]
            fret = string_fret[1]
            TABs[string].append((i, fret))  # Store the position and fret
            note_started = True
        elif condensed_data[i, j] == 0: # if not present reset note_started flag
            note_started = False

# Format TABs with measures
